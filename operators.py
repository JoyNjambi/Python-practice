#arithmetic operators
num1=45
num2=72

print(num1+num2)
print(num1-num2)
print(num1*num2)
print(num1/num2)
print(num1%num2)

#comparison operator
x=6
y=10
z=45.78
print(x==y) #equals
print(x>y) #greater
print(x>=y) #greater or equal to
print(x<=y) #less than or equal to
print(x!=y) #not equal to

#logical operator
print(x>y and x==6)
print(x>y or x==6)
print(z>y and z<x)
print(z==x or y<z)
print(y==x and z>y)



